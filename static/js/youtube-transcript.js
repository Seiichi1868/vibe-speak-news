/**
 * Browser-side YouTube transcript fetcher (InnerTube ANDROID client).
 * Requests go from the user's browser directly to YouTube.
 */
(function initYoutubeTranscript(global) {
  "use strict";

  const VIDEO_ID_RE = /^[a-zA-Z0-9_-]{11}$/;
  const BROWSER_UA =
    "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36";
  const ANDROID_UA = "com.google.android.youtube/20.10.38 (Linux; U; Android 14) gzip";
  const ANDROID_CLIENT_VERSION = "20.10.38";
  const DEFAULT_LANGUAGES = ["ja", "en"];
  const DEFAULT_TIMEOUT_MS = 30000;
  const DEFAULT_MAX_RETRIES = 3;
  const LANGUAGE_LABELS = { ja: "Japanese", en: "English" };

  function extractVideoId(input) {
    const raw = String(input || "").trim();
    if (!raw) throw new Error("YouTube URL または動画 ID を入力してください。");
    if (VIDEO_ID_RE.test(raw)) return raw;

    const decoded = decodeURIComponent(raw.split("#")[0]);
    const match = decoded.match(/(?:v=|vi=|\/)([0-9A-Za-z_-]{11})(?=$|[^0-9A-Za-z_-])/i);
    if (match && VIDEO_ID_RE.test(match[1])) return match[1];

    throw new Error(
      "有効な YouTube URL または 11 桁の動画 ID を入力してください。（watch?v= / youtu.be/ / shorts/ に対応）"
    );
  }

  function languageLabel(code) {
    const base = String(code || "").split("-")[0].toLowerCase();
    return LANGUAGE_LABELS[base] || code || "Unknown";
  }

  function langMatches(candidate, preferred) {
    const c = String(candidate || "").toLowerCase();
    const p = String(preferred || "").toLowerCase();
    return c === p || c.startsWith(`${p}-`);
  }

  function extractApiKey(html) {
    const match = html.match(/"INNERTUBE_API_KEY"\s*:\s*"([^"]+)"/);
    return match ? match[1] : null;
  }

  function extractTracksFromHtml(html) {
    const match = html.match(/var ytInitialPlayerResponse\s*=\s*(\{)/);
    if (!match) return null;

    const start = (match.index || 0) + match[0].length - 1;
    let depth = 0;
    let end = start;
    for (let i = start; i < html.length; i += 1) {
      if (html[i] === "{") depth += 1;
      else if (html[i] === "}") {
        depth -= 1;
        if (depth === 0) {
          end = i + 1;
          break;
        }
      }
    }

    try {
      const data = JSON.parse(html.slice(start, end));
      const tracks = data?.captions?.playerCaptionsTracklistRenderer?.captionTracks;
      return tracks?.length ? tracks : null;
    } catch (_err) {
      return null;
    }
  }

  function selectCaptionTrack(tracks, preferredLanguages, includeAutoGenerated) {
    if (!tracks?.length) return null;
    const manual = tracks.filter((track) => track.kind !== "asr");
    const auto = tracks.filter((track) => track.kind === "asr");
    const searchOrder = includeAutoGenerated ? [...manual, ...auto] : manual;

    for (const lang of preferredLanguages) {
      const exact = searchOrder.find((track) => langMatches(track.languageCode, lang));
      if (exact) return exact;
    }
    return searchOrder[0] || null;
  }

  function parseTimedTextEvents(data) {
    const snippets = [];
    for (const event of data?.events || []) {
      if (!event?.segs) continue;
      const text = event.segs
        .map((seg) => seg.utf8 || "")
        .join("")
        .replace(/\n/g, " ")
        .trim();
      if (!text) continue;
      snippets.push({
        start: Number(event.tStartMs || 0) / 1000,
        duration: Math.max(Number(event.dDurationMs || 0) / 1000, 0.1),
        text,
      });
    }
    return snippets;
  }

  function normalizeSnippets(snippets) {
    return (snippets || [])
      .map((snippet) => ({
        start: Math.round(Number(snippet.start || 0) * 1000) / 1000,
        duration: Math.round(Number(snippet.duration || 0) * 1000) / 1000,
        text: String(snippet.text || "").trim(),
      }))
      .filter((snippet) => snippet.text);
  }

  function filterSnippetsByRange(snippets, startSec, endSec) {
    if (endSec <= startSec) return snippets;
    return snippets.filter((snippet) => {
      const snippetStart = Number(snippet.start || 0);
      const snippetEnd = snippetStart + Number(snippet.duration || 0);
      return snippetStart < endSec && snippetEnd > startSec;
    });
  }

  function formatTranscriptTimestamp(sec) {
    const s = Math.max(0, Math.floor(Number(sec) || 0));
    const hours = Math.floor(s / 3600);
    const minutes = Math.floor((s % 3600) / 60);
    const seconds = s % 60;
    if (hours) return `${hours}:${String(minutes).padStart(2, "0")}:${String(seconds).padStart(2, "0")}`;
    return `${minutes}:${String(seconds).padStart(2, "0")}`;
  }

  function formatScriptForLesson(snippets) {
    const lines = [];
    let current = [];
    for (const snippet of snippets) {
      const text = String(snippet.text || "").trim();
      if (!text) continue;
      current.push(text);
      if (/[.!?]$/.test(text)) {
        lines.push(current.join(" "));
        current = [];
      }
    }
    if (current.length) lines.push(current.join(" "));
    return lines.join("\n");
  }

  function formatTimedScript(snippets) {
    return snippets
      .map((snippet) => `${formatTranscriptTimestamp(snippet.start)}  ${snippet.text}`)
      .join("\n");
  }

  function isLikelyCorsError(err) {
    const message = String(err?.message || err || "").toLowerCase();
    return (
      err instanceof TypeError ||
      message.includes("failed to fetch") ||
      message.includes("networkerror") ||
      message.includes("cors")
    );
  }

  function translateFetchError(err) {
    if (isLikelyCorsError(err)) {
      return new Error(
        "ブラウザのセキュリティ制限（CORS）により、管理画面から YouTube へ直接接続できませんでした。" +
          "手動でスクリプトを貼り付けるか、Cloudflare Workers 等のエッジプロキシを検討してください。"
      );
    }
    const message = String(err?.message || err || "").toLowerCase();
    if (message.includes("429") || message.includes("rate limit")) {
      return new Error("YouTube へのリクエストが制限されています。しばらく待ってから再試行してください。");
    }
    if (message.includes("blocked") || message.includes("sign in to confirm")) {
      return new Error("YouTube から字幕を取得できませんでした。動画が非公開・年齢制限の可能性があります。");
    }
    if (message.includes("no suitable caption") || message.includes("no captions")) {
      return new Error("日本語・英語の字幕が見つかりませんでした。");
    }
    return err instanceof Error ? err : new Error(String(err || "字幕の取得に失敗しました。"));
  }

  async function withTimeout(timeoutMs, operation) {
    const controller = new AbortController();
    const timer = setTimeout(() => controller.abort(), timeoutMs);
    try {
      return await operation(controller.signal);
    } finally {
      clearTimeout(timer);
    }
  }

  async function fetchWithRetry(url, options, maxRetries) {
    let lastError = null;
    for (let attempt = 0; attempt < maxRetries; attempt += 1) {
      try {
        const response = await fetch(url, options);
        if ([408, 425, 429, 500, 502, 503, 504].includes(response.status) && attempt < maxRetries - 1) {
          await new Promise((resolve) => setTimeout(resolve, 2 ** attempt * 1000));
          continue;
        }
        return response;
      } catch (err) {
        lastError = err;
        if (isLikelyCorsError(err) || attempt >= maxRetries - 1) break;
        await new Promise((resolve) => setTimeout(resolve, 2 ** attempt * 1000));
      }
    }
    throw translateFetchError(lastError || new Error("字幕の取得に失敗しました。"));
  }

  async function fetchTracksViaAndroid(videoId, apiKey, timeoutMs, maxRetries, signal) {
    const response = await fetchWithRetry(
      `https://www.youtube.com/youtubei/v1/player?key=${encodeURIComponent(apiKey)}&prettyPrint=false`,
      {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          "User-Agent": ANDROID_UA,
          "X-YouTube-Client-Name": "3",
          "X-YouTube-Client-Version": ANDROID_CLIENT_VERSION,
        },
        body: JSON.stringify({
          context: {
            client: {
              clientName: "ANDROID",
              clientVersion: ANDROID_CLIENT_VERSION,
              hl: "en",
              gl: "US",
            },
          },
          videoId,
        }),
        signal,
      },
      maxRetries
    );

    if (!response.ok) return null;
    const data = await response.json();
    const tracks = data?.captions?.playerCaptionsTracklistRenderer?.captionTracks;
    return tracks?.length ? tracks : null;
  }

  async function fetchCaptionTracks(videoId, timeoutMs, maxRetries) {
    const pageResp = await fetchWithRetry(
      `https://www.youtube.com/watch?v=${encodeURIComponent(videoId)}`,
      {
        headers: {
          "User-Agent": BROWSER_UA,
          "Accept-Language": "en-US,en;q=0.9",
          Accept: "text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8",
        },
      },
      maxRetries
    );

    if (!pageResp.ok) {
      throw new Error("日本語・英語の字幕が見つかりませんでした。");
    }

    const html = await pageResp.text();
    const apiKey = extractApiKey(html);
    if (apiKey) {
      const tracks = await withTimeout(timeoutMs, (signal) =>
        fetchTracksViaAndroid(videoId, apiKey, timeoutMs, maxRetries, signal)
      );
      if (tracks) return tracks;
    }

    const htmlTracks = extractTracksFromHtml(html);
    if (htmlTracks) return htmlTracks;

    throw new Error("日本語・英語の字幕が見つかりませんでした。");
  }

  async function fetchCaptionTrack(url, maxRetries, signal) {
    const jsonUrl = `${url.replace(/&fmt=[^&]*/g, "")}&fmt=json3`;
    const response = await fetchWithRetry(
      jsonUrl,
      {
        headers: {
          "User-Agent": BROWSER_UA,
          Referer: "https://www.youtube.com/",
        },
        signal,
      },
      maxRetries
    );

    if (!response.ok) {
      throw new Error(`字幕ファイルの取得に失敗しました (HTTP ${response.status})`);
    }

    const body = await response.text();
    if (!body) throw new Error("字幕データが空でした。");

    let data;
    try {
      data = JSON.parse(body);
    } catch (_err) {
      throw new Error("字幕データを解析できませんでした。");
    }

    const snippets = parseTimedTextEvents(data);
    if (!snippets.length) throw new Error("字幕データを解析できませんでした。");
    return snippets;
  }

  async function fetchTranscript(videoIdOrUrl, options) {
    const opts = options || {};
    const languages = opts.languages || DEFAULT_LANGUAGES;
    const timeoutMs = opts.timeoutMs || DEFAULT_TIMEOUT_MS;
    const maxRetries = opts.maxRetries || DEFAULT_MAX_RETRIES;
    const startSec = opts.startSec ?? null;
    const endSec = opts.endSec ?? null;

    const videoId = extractVideoId(videoIdOrUrl);
    const tracks = await fetchCaptionTracks(videoId, timeoutMs, maxRetries);
    const selectedTrack = selectCaptionTrack(tracks, languages, true);
    if (!selectedTrack?.baseUrl) {
      throw new Error("日本語・英語の字幕が見つかりませんでした。");
    }

    const fetchedSnippets = await withTimeout(timeoutMs, (signal) =>
      fetchCaptionTrack(selectedTrack.baseUrl, maxRetries, signal)
    );

    let snippets = fetchedSnippets;
    let rangeApplied = false;
    if (startSec !== null && endSec !== null && Number(endSec) > Number(startSec)) {
      snippets = filterSnippetsByRange(fetchedSnippets, Number(startSec), Number(endSec));
      rangeApplied = true;
      if (!snippets.length) {
        throw new Error("指定した時間範囲に文字起こしがありません。");
      }
    }

    const allSnippets = normalizeSnippets(fetchedSnippets);
    const activeSnippets = normalizeSnippets(snippets);

    return {
      video_id: videoId,
      language: languageLabel(selectedTrack.languageCode),
      language_code: selectedTrack.languageCode,
      is_generated: selectedTrack.kind === "asr",
      script: formatScriptForLesson(activeSnippets),
      script_timed: formatTimedScript(activeSnippets),
      snippets: activeSnippets,
      all_snippets: rangeApplied ? allSnippets : activeSnippets,
      range_applied: rangeApplied,
      start_sec: rangeApplied ? Number(startSec) : null,
      end_sec: rangeApplied ? Number(endSec) : null,
    };
  }

  global.YoutubeTranscript = {
    extractVideoId,
    fetchTranscript,
    filterSnippetsByRange,
    formatScriptForLesson,
    formatTimedScript,
    normalizeSnippets,
  };
})(window);
